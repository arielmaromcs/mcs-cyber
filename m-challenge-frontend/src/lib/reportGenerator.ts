function scoreColor(score: number): string {
  if (score >= 80) return '#10b981';
  if (score >= 60) return '#3b82f6';
  if (score >= 40) return '#eab308';
  return '#ef4444';
}

function severityBadge(sev: string): string {
  return `<span class="badge badge-${sev}">${sev}</span>`;
}

function generateHeader(title: string, target: string, date: string): string {
  return `<div class="header">
    <div class="brand">M-Challenge Security Scanner</div>
    <h1>${title}</h1>
    <div class="sub">${target} &mdash; ${date}</div>
  </div>`;
}

function generateFooter(): string {
  return `<div class="footer">
    Generated by M-Challenge Security Scanner &mdash; ${new Date().toLocaleString()} &mdash; Confidential
  </div>`;
}

// ─── WEB SCAN REPORT ───
export function generateWebReport(result: any, target: string): string {
  const findings = result.findings || [];
  const exposures = result.exposureFindings || result.exposure_findings || [];
  const webScore = result.webSecurityScore || result.web_security_score || 0;
  const dnsScore = result.dnsSecurityScore || result.dns_security_score || 0;
  const riskScore = result.riskScore || result.risk_score || 0;
  const counts = result.findingsCount || result.findings_count || {};
  const tech = result.technologies || {};

  return `<!DOCTYPE html><html><head><meta charset="utf-8"><title>Web Security Report - ${target}</title><style>${STYLES}</style></head><body>
  <div class="container">
    ${generateHeader('Web Security Scan Report', target, new Date().toLocaleDateString())}
    <div class="score-section">
      <div class="score-card"><div class="value" style="color:${scoreColor(100 - riskScore)}">${100 - riskScore}</div><div class="label">Overall Score</div></div>
      <div class="score-card"><div class="value" style="color:${scoreColor(webScore)}">${webScore}</div><div class="label">Web Security</div></div>
      <div class="score-card"><div class="value" style="color:${scoreColor(dnsScore)}">${dnsScore}</div><div class="label">DNS Security</div></div>
      <div class="score-card"><div class="value blue">${result.pagesScanned || result.pages_scanned || 0}</div><div class="label">Pages Scanned</div></div>
    </div>

    <div class="section"><h2>Findings Summary</h2>
      <div class="score-section">
        <div class="score-card"><div class="value red">${counts.critical || 0}</div><div class="label">Critical</div></div>
        <div class="score-card"><div class="value" style="color:#f97316">${counts.high || 0}</div><div class="label">High</div></div>
        <div class="score-card"><div class="value yellow">${counts.medium || 0}</div><div class="label">Medium</div></div>
        <div class="score-card"><div class="value blue">${counts.low || 0}</div><div class="label">Low</div></div>
      </div>
    </div>

    ${tech.server || tech.framework ? `<div class="section"><h2>Technologies Detected</h2>
      <div class="two-col">
        ${tech.server ? `<div class="info-row"><span class="k">Server</span><span class="v">${tech.server}</span></div>` : ''}
        ${tech.framework ? `<div class="info-row"><span class="k">Framework</span><span class="v">${tech.framework}</span></div>` : ''}
        ${tech.cdn ? `<div class="info-row"><span class="k">CDN</span><span class="v">${tech.cdn}</span></div>` : ''}
        ${tech.waf ? `<div class="info-row"><span class="k">WAF</span><span class="v">${tech.waf}</span></div>` : ''}
      </div>
    </div>` : ''}

    <div class="section"><h2>All Findings (${findings.length})</h2>
      ${findings.sort((a: any, b: any) => {
        const order: Record<string, number> = { critical: 0, high: 1, medium: 2, low: 3, info: 4 };
        return (order[a.severity] ?? 5) - (order[b.severity] ?? 5);
      }).map((f: any) => `
        <div class="finding-card ${f.severity}">
          <div class="finding-title">${severityBadge(f.severity)} ${f.title}</div>
          <div class="finding-desc">${f.description || ''}</div>
          ${f.recommendation ? `<div class="finding-rec">💡 ${f.recommendation}</div>` : ''}
        </div>
      `).join('')}
    </div>

    ${exposures.length > 0 ? `<div class="section"><h2>Exposure Discovery (${exposures.length})</h2>
      <table><thead><tr><th>Path</th><th>Status</th><th>Severity</th><th>Source</th></tr></thead><tbody>
        ${exposures.slice(0, 50).map((e: any) => `<tr><td><code>${e.path}</code></td><td>${e.status_code}</td><td>${severityBadge(e.severity)}</td><td>${e.source || ''}</td></tr>`).join('')}
      </tbody></table>
    </div>` : ''}

    ${generateFooter()}
  </div></body></html>`;
}

