// ─── THREAT INTEL REPORT ───
export function generateThreatReport(scanData: any, target: string): string {
  const ports = scanData.open_ports || scanData.openPorts || [];
  const services = scanData.services || [];

  return `<!DOCTYPE html><html><head><meta charset="utf-8"><title>Threat Intelligence Report - ${target}</title><style>${STYLES}</style></head><body>
  <div class="container">
    ${generateHeader('Port Exposure Scan Report', target, new Date().toLocaleDateString())}
    <div class="score-section">
      <div class="score-card"><div class="value" style="color:${ports.length > 5 ? '#ef4444' : ports.length > 0 ? '#eab308' : '#10b981'}">${ports.length}</div><div class="label">Open Ports</div></div>
      <div class="score-card"><div class="value blue">${services.length || ports.length}</div><div class="label">Services Detected</div></div>
    </div>

    ${ports.length > 0 ? `<div class="section"><h2>Open Ports &amp; Services</h2>
      <table><thead><tr><th>Port</th><th>Protocol</th><th>Service</th><th>Version</th></tr></thead><tbody>
        ${ports.map((p: any) => {
          if (typeof p === 'object') {
            return `<tr><td><strong>${p.port || p.portid || ''}</strong></td><td>${p.protocol || 'tcp'}</td><td>${p.service || p.name || ''}</td><td>${p.version || p.product || ''}</td></tr>`;
          }
          return `<tr><td><strong>${p}</strong></td><td>tcp</td><td>-</td><td>-</td></tr>`;
        }).join('')}
      </tbody></table>
    </div>` : '<div class="section"><h2>Results</h2><p style="color:#10b981">No open ports detected — target appears well-secured.</p></div>'}

    ${generateFooter()}
  </div></body></html>`;
}

// ─── MITRE REPORT ───
export function generateMitreReport(data: any, target: string): string {
  const techniques = data.techniques || [];
  const attackerView = data.attacker_view || {};
  const remediations = data.remediation_plan || {};

  return `<!DOCTYPE html><html><head><meta charset="utf-8"><title>MITRE ATT&CK Report - ${target}</title><style>${STYLES}</style></head><body>
  <div class="container">
    ${generateHeader('MITRE ATT&CK Analysis Report', target, new Date().toLocaleDateString())}
    <div class="score-section">
      <div class="score-card"><div class="value blue">${techniques.length}</div><div class="label">Techniques Mapped</div></div>
      <div class="score-card"><div class="value" style="color:${scoreColor(data.overall_score || 50)}">${data.overall_score || '-'}</div><div class="label">Security Score</div></div>
    </div>

    ${attackerView.detailed_scenario ? `<div class="section"><h2>Attacker Scenario</h2>
      <div class="finding-card medium"><div class="finding-desc">${attackerView.detailed_scenario}</div></div>
    </div>` : ''}

    ${(attackerView.attack_narrative || []).length > 0 ? `<div class="section"><h2>Attack Path</h2>
      ${attackerView.attack_narrative.map((step: any, i: number) => `
        <div class="finding-card ${i < 2 ? 'low' : i < 4 ? 'medium' : 'high'}">
          <div class="finding-title">Step ${step.step || i + 1}: ${step.action || ''}</div>
          <div class="finding-desc">${step.detail || ''}</div>
          ${step.technique ? `<div class="finding-rec">MITRE: ${step.technique}</div>` : ''}
        </div>
      `).join('')}
    </div>` : ''}

    ${techniques.length > 0 ? `<div class="section"><h2>MITRE ATT&CK Techniques</h2>
      <table><thead><tr><th>ID</th><th>Technique</th><th>Tactic</th><th>Severity</th></tr></thead><tbody>
        ${techniques.map((t: any) => `<tr><td><strong>${t.technique_id || t.id || ''}</strong></td><td>${t.name || t.technique_name || ''}</td><td>${t.tactic || ''}</td><td>${severityBadge(t.severity || 'medium')}</td></tr>`).join('')}
      </tbody></table>
    </div>` : ''}

    ${(remediations.short_term || remediations.immediate || []).length > 0 ? `<div class="section"><h2>Remediation Plan</h2>
      <h3>Immediate Actions (30 days)</h3>
      ${(remediations.short_term || remediations.immediate || []).map((r: any) => `<div class="finding-card high"><div class="finding-desc">${typeof r === 'string' ? r : r.action || r.description || ''}</div></div>`).join('')}
      ${(remediations.medium_term || []).length > 0 ? `<h3>Medium Term (60 days)</h3>
      ${remediations.medium_term.map((r: any) => `<div class="finding-card medium"><div class="finding-desc">${typeof r === 'string' ? r : r.action || r.description || ''}</div></div>`).join('')}` : ''}
    </div>` : ''}

    ${generateFooter()}
  </div></body></html>`;
}

// ─── DOWNLOAD HELPER ───
export function downloadReport(html: string, filename: string) {
  const blob = new Blob([html], { type: 'text/html;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
